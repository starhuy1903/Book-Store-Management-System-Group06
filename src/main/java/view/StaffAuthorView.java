    private void getModel() {
        model = new DefaultTableModel(StaffAuthorView.COLUMN_NAME, 0);
        setModelData();
    }

    private void setModelData() {
        AuthorController ac = new AuthorController();
        List<Author> list = ac.getAll();

        model.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            Object[] obj = {list.get(i).getId(), list.get(i).getName(), list.get(i).getStatus(), Boolean.FALSE};
            model.addRow(obj);
        }
    }
